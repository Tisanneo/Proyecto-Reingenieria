    private void cargarDatos() {
        id.setText(cuenta.getId());
        viejoUsername.setText(cuenta.getNombreCliente());
        nuevoUsername.setText("");
    }

    private Cuenta buscarCuenta(String id) {
        return GestorCuentas.buscarCuenta(id);
    }

    private void guardarCambios() {
        ArrayList<Cuenta> lista = GestorCuentas.cargarCuentas();
        for (int x = 0; x<lista.size(); x++) {
            if (lista.get(x).getId().equals(cuenta.getId())) {
                lista.get(x).setNombreCliente(cuenta.getNombreCliente());
                break;
            }}
        try {
            GestorCuentas.guardarCuentas(lista);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Err");
        }
    }
    
