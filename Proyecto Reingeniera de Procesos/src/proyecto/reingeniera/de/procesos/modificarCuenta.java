    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        String nuevo = nuevoUsername.getText().trim();

        if (nuevo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Escribe un nombre.");
            return;
        }

        if (nuevo.equals(cuenta.getNombreCliente())) {
            JOptionPane.showMessageDialog(this, "El nombre es el mismo al anterior.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this,"Seguro de Cambiar el nombre de '" + cuenta.getNombreCliente() +"?","Confirmar",JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.YES_OPTION) {
            cuenta.setNombreCliente(nuevo);
            guardarCambios();
            JOptionPane.showMessageDialog(this, "Nombre cambiad.");
            this.dispose(); 
        }
    }//GEN-LAST:event_aceptarActionPerformed

